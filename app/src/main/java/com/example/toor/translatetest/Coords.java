package com.example.toor.translatetest;

public class Coords {

    private final float x;
    private final float y;

    public Coords(final float x, final float y) {
        this.x = x;
        this.y = y;
    }

    public float getX() {
        return x;
    }

    public float getY() {
        return y;
    }


}
